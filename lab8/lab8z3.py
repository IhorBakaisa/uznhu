# Використовуючи підпрограму для знаходження n-тового числа Фібоначчі.
# Обчислити значення виразу s = f[3]+f[8], де f[i] -- i-тове число Фібоначчі.

def f(i):
    if i == 1 or i == 2:
        return 1
    else:
        return f(i-1)+f(i-2)

s = f(3)+f(8)
print(f"Результат виразу s = {s}")